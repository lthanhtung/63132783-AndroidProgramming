package com.lethanhtung.ontaprc;

public class youtuber {
    String ten;
    String img;

    public youtuber(String ten, String img) {
        this.ten = ten;
        this.img = img;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }
}

